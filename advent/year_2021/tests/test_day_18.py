def test_split_all():
    number = [[[[0,7],4],[15,[0,13]]],[1,1]]

    changed = split(number)

    assert changed
    assert number == [[[[0,7],4],[[7,8],[0,13]]],[1,1]]
    
    changed = split(number)

    assert changed
    assert number == [[[[0,7],4],[[7,8],[0,[6,7]]]],[1,1]]

def test_reduce():
    number = [[[[[4,3],4],4],[7,[[8,4],9]]],[1,1]]

    reduce(number)

    assert number == [[[[0,7],4],[[7,8],[6,0]]],[8,1]]

